from __future__ import annotations

import uuid
from datetime import date
from decimal import Decimal
from typing import TYPE_CHECKING

from sqlalchemy import Date, Numeric, ForeignKey
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.models.base import BaseModel

if TYPE_CHECKING:
    from app.models.forecast_model import ForecastModel


class ForecastResult(BaseModel):
    """
    Represents a prediction generated by a forecasting model.
    """

    __tablename__ = "forecast_results"

    forecast_model_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("forecast_models.id"),
        nullable=False,
        index=True,
    )

    prediction_date: Mapped[date] = mapped_column(
        Date,
        nullable=False,
    )

    predicted_value: Mapped[Decimal] = mapped_column(
        Numeric(18, 4),
        nullable=False,
    )

    confidence_interval: Mapped[dict | None] = mapped_column(
        JSONB,
        nullable=True,
    )

    actual_value: Mapped[Decimal | None] = mapped_column(
        Numeric(18, 4),
        nullable=True,
    )

    forecast_model: Mapped["ForecastModel"] = relationship(
        "ForecastModel",
        back_populates="results",
        lazy="selectin",
    )
